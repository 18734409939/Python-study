print('------对象的布尔值--------')
print(bool(False))     #False
print(bool(0))         #数值0
print(bool(None))      #None
print(bool(''))        #空字符串
print(bool([]))        #空列表
print(bool(list()))    #空列表
print(bool(()))        #空元组
print(bool(tuple()))   #空元组
print(bool({}))        #空字典







